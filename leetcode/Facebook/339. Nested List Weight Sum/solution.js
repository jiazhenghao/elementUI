/**
 * @param {NestedInteger[]} nestedList
 * @return {number}
 */

var depthSum = function (nestedList) {
    return depthS(nestedList, 1);

    function depthS(list, depth) {
        var sum = 0;
        for (let n of list) {
            if (n.isInteger()) {
                sum += n.getInteger() * depth;
            } else {
                sum += depthS(n.getList(), depth + 1);
            }
        }
        return sum;
    }
};
